/**
 * op_add - helper function
 *
 * Description: adds two ints
 * @a: first int
 * @b: second int
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - helper function
 *
 * Description: subtracts two ints
 * @a: first int
 * @b: second int
 * Return: result
 */


int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - helper function
 *
 * Description: multiplies two ints
 * @a: first int
 * @b: second int
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - helper function
 *
 * Description: divides two ints
 * @a: first int
 * @b: second int
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - helper function
 *
 * Description: mods two ints
 * @a: first int
 * @b: second int
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
